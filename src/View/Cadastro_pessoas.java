    private void Camp_CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Camp_CadastrarActionPerformed
        String nome = Camp_Nome.getText ();
        String CPF = Camp_CPF.getText();
        String Dia =  Camp_dia.getText();
        String Mes = Camp_mes.getText();
        String Ano = Camp_ano.getText();
        String Fone = Camp_fone.getText();
        String Aniversario = Dia + "/" + Mes + "/" + Ano;
        
        if (nome.isEmpty() || CPF.isEmpty() || Aniversario.isEmpty() || Dia.isEmpty() || Mes.isEmpty() || Ano.isEmpty() ) {
            JOptionPane.showMessageDialog(null, "Faltam dados para ser inseridos!", "FALTAM DADOS!", JOptionPane.WARNING_MESSAGE);
        } else {
             boolean isValid = Model.CPF.Validar_cpf(CPF);
            if (isValid == true) {
                System.out.println("O CPF " + CPF + " √© v√°lido.");
                 try {
                     Pessoa P = new Pessoa (nome, CPF, Aniversario, Fone );
                     int idade = P.getIdade();
                      if (idade >= 120) {
                    JOptionPane.showMessageDialog(null, "A idade n√£o pode ser maior ou igual a 120 anos!", "Idade Inv√°lida", JOptionPane.ERROR_MESSAGE);
                } else {
                     JOptionPane.showMessageDialog(null, ("Nome: "+P.getNome()+"\nCPF: "+P.getCPF()+"\nData Anivers√°rio: "+P.getAniversario()+"\nIdade: "+P.getIdade()+"\nTelefone: "+P.getTelefone()), "Pessoa Cadastrada no Sistema!", JOptionPane.INFORMATION_MESSAGE);
                     limpar();
                      }
                 } catch (ParseException ex) {
                     Logger.getLogger(Cadastro_pessoas.class.getName()).log(Level.SEVERE, null, ex);
                 }
            } else {
                JOptionPane.showMessageDialog(null, "CPF √© invalido!", "Dado incorreto!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_Camp_CadastrarActionPerformed
